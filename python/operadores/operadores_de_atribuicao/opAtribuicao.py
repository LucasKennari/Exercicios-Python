# # Sao operadores utilizados para definir o valor iniciall ou sobrescrever 
# o valor de uma variável

# O sinal de IGUAL "=" é um sinal de atribuicao

saldo1 = 500
print(saldo1) 

# # Atribuicao com adicao

# O sinal de MAIS com IGUAL "+=" significa que irá atribuir e somar o valor 

saldo2 = 500
saldo2 += 200
print (saldo2) # Resposta : 700

# # Atribuicao com subtracao

saldo3 = 500
saldo3 -= 100
print(saldo3) # REesposta 400 // saldo = saldo - 100

# # Atribuicao com multiplicacao

saldo4 = 500
saldo4 *= 100
print(saldo4) # saldo = saldo * 100

# # Atribuicao com divisao

saldo5 = 500
saldo5 /= 5
print(saldo5) #  saldo = saldo / 5

saldo6 = 500
saldo6 //= 5
print(saldo6) #  saldo = saldo // 5


# # Atribuicao com modulo

saldo7 = 500
saldo7 %= 400

print(saldo7) # saldo = saldo % 400


# Atribuicao com exponenciacao

saldo8 = 80
saldo8 **= 2

print(saldo8) # saldo = saldo ** 2